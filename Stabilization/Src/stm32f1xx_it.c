/**
* @brief This function handles USART1 global interrupt.
*/
void USART1_IRQHandler(void)
{
	static BaseType_t xHigherPriorityTaskWoken;	//переменная для отправки данных в очередь из прерывания
	//проверка, произошло ли прерывание
	if(USART1->SR&USART_SR_IDLE)
	{	
		//чтение регистра DR
		(void)USART1->DR;
		DMA1_Channel5->CCR &= ~DMA_CCR_EN;  //отключение канала DMA                       
		DMA1_Channel5->CMAR = (uint32_t)DMA_BUFFER; //переустановка адреса буфера для приема сообщения
    DMA1_Channel5->CNDTR = DMA_BUFFER_SIZE; //переустановка размера буфера
		//если сообщение начинается с символа "+", значит оно принято с компьютера
		if(DMA_BUFFER[2] == '+')
		{
			xHigherPriorityTaskWoken = pdFALSE; //установка значения pdFALSE для отправки данных в очередь из прерывания
			//копирование принятого сообщения в массив для отправки в очередь
			for(uint8_t i=0; i<25; i++)
				{
					DMA[i] = DMA_BUFFER[i+2];
				}
			//отправка данных в очередь
			xQueueSendFromISR(xQueueSerialDataReceived, &DMA, &xHigherPriorityTaskWoken);		
		}	
		//очистка буфера
		for(uint8_t i=0; i<25; i++)
			{
				DMA_BUFFER[i] = 0;
			}
			DMA1_Channel5->CCR |= DMA_CCR_EN; //включение канала DMA         	
	}
  HAL_UART_IRQHandler(&huart1);
}

